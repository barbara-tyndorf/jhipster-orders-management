package pl.com.web.rest;

import pl.com.OrdersManagementApp;
import pl.com.domain.Order;
import pl.com.repository.OrderRepository;
import pl.com.web.rest.errors.ExceptionTranslator;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.mockito.MockitoAnnotations;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.data.web.PageableHandlerMethodArgumentResolver;
import org.springframework.http.MediaType;
import org.springframework.http.converter.json.MappingJackson2HttpMessageConverter;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;
import org.springframework.transaction.annotation.Transactional;
import org.springframework.validation.Validator;

import javax.persistence.EntityManager;
import java.math.BigDecimal;
import java.util.List;

import static pl.com.web.rest.TestUtil.createFormattingConversionService;
import static org.assertj.core.api.Assertions.assertThat;
import static org.hamcrest.Matchers.hasItem;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.*;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.*;

import pl.com.domain.enumeration.Currency;
import pl.com.domain.enumeration.Currency;
/**
 * Integration tests for the {@link OrderResource} REST controller.
 */
@SpringBootTest(classes = OrdersManagementApp.class)
public class OrderResourceIT {

    private static final BigDecimal DEFAULT_CUSTOMER_PRICE = new BigDecimal(1);
    private static final BigDecimal UPDATED_CUSTOMER_PRICE = new BigDecimal(2);

    private static final BigDecimal DEFAULT_CARRIER_PRICE = new BigDecimal(1);
    private static final BigDecimal UPDATED_CARRIER_PRICE = new BigDecimal(2);

    private static final Currency DEFAULT_CUSTOMER_CURRENCY = Currency.PLN;
    private static final Currency UPDATED_CUSTOMER_CURRENCY = Currency.EUR;

    private static final Currency DEFAULT_CARRIER_CURRENCY = Currency.PLN;
    private static final Currency UPDATED_CARRIER_CURRENCY = Currency.EUR;

    @Autowired
    private OrderRepository orderRepository;

    @Autowired
    private MappingJackson2HttpMessageConverter jacksonMessageConverter;

    @Autowired
    private PageableHandlerMethodArgumentResolver pageableArgumentResolver;

    @Autowired
    private ExceptionTranslator exceptionTranslator;

    @Autowired
    private EntityManager em;

    @Autowired
    private Validator validator;

    private MockMvc restOrderMockMvc;

    private Order order;

    @BeforeEach
    public void setup() {
        MockitoAnnotations.initMocks(this);
        final OrderResource orderResource = new OrderResource(orderRepository);
        this.restOrderMockMvc = MockMvcBuilders.standaloneSetup(orderResource)
            .setCustomArgumentResolvers(pageableArgumentResolver)
            .setControllerAdvice(exceptionTranslator)
            .setConversionService(createFormattingConversionService())
            .setMessageConverters(jacksonMessageConverter)
            .setValidator(validator).build();
    }

    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Order createEntity(EntityManager em) {
        Order order = new Order()
            .customerPrice(DEFAULT_CUSTOMER_PRICE)
            .carrierPrice(DEFAULT_CARRIER_PRICE)
            .customerCurrency(DEFAULT_CUSTOMER_CURRENCY)
            .carrierCurrency(DEFAULT_CARRIER_CURRENCY);
        return order;
    }
    /**
     * Create an updated entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Order createUpdatedEntity(EntityManager em) {
        Order order = new Order()
            .customerPrice(UPDATED_CUSTOMER_PRICE)
            .carrierPrice(UPDATED_CARRIER_PRICE)
            .customerCurrency(UPDATED_CUSTOMER_CURRENCY)
            .carrierCurrency(UPDATED_CARRIER_CURRENCY);
        return order;
    }

    @BeforeEach
    public void initTest() {
        order = createEntity(em);
    }

    @Test
    @Transactional
    public void createOrder() throws Exception {
        int databaseSizeBeforeCreate = orderRepository.findAll().size();

        // Create the Order
        restOrderMockMvc.perform(post("/api/orders")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(order)))
            .andExpect(status().isCreated());

        // Validate the Order in the database
        List<Order> orderList = orderRepository.findAll();
        assertThat(orderList).hasSize(databaseSizeBeforeCreate + 1);
        Order testOrder = orderList.get(orderList.size() - 1);
        assertThat(testOrder.getCustomerPrice()).isEqualTo(DEFAULT_CUSTOMER_PRICE);
        assertThat(testOrder.getCarrierPrice()).isEqualTo(DEFAULT_CARRIER_PRICE);
        assertThat(testOrder.getCustomerCurrency()).isEqualTo(DEFAULT_CUSTOMER_CURRENCY);
        assertThat(testOrder.getCarrierCurrency()).isEqualTo(DEFAULT_CARRIER_CURRENCY);
    }

    @Test
    @Transactional
    public void createOrderWithExistingId() throws Exception {
        int databaseSizeBeforeCreate = orderRepository.findAll().size();

        // Create the Order with an existing ID
        order.setId(1L);

        // An entity with an existing ID cannot be created, so this API call must fail
        restOrderMockMvc.perform(post("/api/orders")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(order)))
            .andExpect(status().isBadRequest());

        // Validate the Order in the database
        List<Order> orderList = orderRepository.findAll();
        assertThat(orderList).hasSize(databaseSizeBeforeCreate);
    }


    @Test
    @Transactional
    public void getAllOrders() throws Exception {
        // Initialize the database
        orderRepository.saveAndFlush(order);

        // Get all the orderList
        restOrderMockMvc.perform(get("/api/orders?sort=id,desc"))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_UTF8_VALUE))
            .andExpect(jsonPath("$.[*].id").value(hasItem(order.getId().intValue())))
            .andExpect(jsonPath("$.[*].customerPrice").value(hasItem(DEFAULT_CUSTOMER_PRICE.intValue())))
            .andExpect(jsonPath("$.[*].carrierPrice").value(hasItem(DEFAULT_CARRIER_PRICE.intValue())))
            .andExpect(jsonPath("$.[*].customerCurrency").value(hasItem(DEFAULT_CUSTOMER_CURRENCY.toString())))
            .andExpect(jsonPath("$.[*].carrierCurrency").value(hasItem(DEFAULT_CARRIER_CURRENCY.toString())));
    }
    
    @Test
    @Transactional
    public void getOrder() throws Exception {
        // Initialize the database
        orderRepository.saveAndFlush(order);

        // Get the order
        restOrderMockMvc.perform(get("/api/orders/{id}", order.getId()))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_UTF8_VALUE))
            .andExpect(jsonPath("$.id").value(order.getId().intValue()))
            .andExpect(jsonPath("$.customerPrice").value(DEFAULT_CUSTOMER_PRICE.intValue()))
            .andExpect(jsonPath("$.carrierPrice").value(DEFAULT_CARRIER_PRICE.intValue()))
            .andExpect(jsonPath("$.customerCurrency").value(DEFAULT_CUSTOMER_CURRENCY.toString()))
            .andExpect(jsonPath("$.carrierCurrency").value(DEFAULT_CARRIER_CURRENCY.toString()));
    }

    @Test
    @Transactional
    public void getNonExistingOrder() throws Exception {
        // Get the order
        restOrderMockMvc.perform(get("/api/orders/{id}", Long.MAX_VALUE))
            .andExpect(status().isNotFound());
    }

    @Test
    @Transactional
    public void updateOrder() throws Exception {
        // Initialize the database
        orderRepository.saveAndFlush(order);

        int databaseSizeBeforeUpdate = orderRepository.findAll().size();

        // Update the order
        Order updatedOrder = orderRepository.findById(order.getId()).get();
        // Disconnect from session so that the updates on updatedOrder are not directly saved in db
        em.detach(updatedOrder);
        updatedOrder
            .customerPrice(UPDATED_CUSTOMER_PRICE)
            .carrierPrice(UPDATED_CARRIER_PRICE)
            .customerCurrency(UPDATED_CUSTOMER_CURRENCY)
            .carrierCurrency(UPDATED_CARRIER_CURRENCY);

        restOrderMockMvc.perform(put("/api/orders")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(updatedOrder)))
            .andExpect(status().isOk());

        // Validate the Order in the database
        List<Order> orderList = orderRepository.findAll();
        assertThat(orderList).hasSize(databaseSizeBeforeUpdate);
        Order testOrder = orderList.get(orderList.size() - 1);
        assertThat(testOrder.getCustomerPrice()).isEqualTo(UPDATED_CUSTOMER_PRICE);
        assertThat(testOrder.getCarrierPrice()).isEqualTo(UPDATED_CARRIER_PRICE);
        assertThat(testOrder.getCustomerCurrency()).isEqualTo(UPDATED_CUSTOMER_CURRENCY);
        assertThat(testOrder.getCarrierCurrency()).isEqualTo(UPDATED_CARRIER_CURRENCY);
    }

    @Test
    @Transactional
    public void updateNonExistingOrder() throws Exception {
        int databaseSizeBeforeUpdate = orderRepository.findAll().size();

        // Create the Order

        // If the entity doesn't have an ID, it will throw BadRequestAlertException
        restOrderMockMvc.perform(put("/api/orders")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(order)))
            .andExpect(status().isBadRequest());

        // Validate the Order in the database
        List<Order> orderList = orderRepository.findAll();
        assertThat(orderList).hasSize(databaseSizeBeforeUpdate);
    }

    @Test
    @Transactional
    public void deleteOrder() throws Exception {
        // Initialize the database
        orderRepository.saveAndFlush(order);

        int databaseSizeBeforeDelete = orderRepository.findAll().size();

        // Delete the order
        restOrderMockMvc.perform(delete("/api/orders/{id}", order.getId())
            .accept(TestUtil.APPLICATION_JSON_UTF8))
            .andExpect(status().isNoContent());

        // Validate the database contains one less item
        List<Order> orderList = orderRepository.findAll();
        assertThat(orderList).hasSize(databaseSizeBeforeDelete - 1);
    }
}
